def parse_consolidated_categories(response_text):
    """–ü–∞—Ä—Å–∏—Ç –∫–æ–Ω—Å–æ–ª–∏–¥–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏"""
    categories = []
    lines = response_text.strip().split('\n')
    
    for line in lines:
        line = line.strip()
        if not line or line.startswith('–ù–∞–∑–≤–∞–Ω–∏–µ') or line.startswith('#'):
            continue
            
        parts = line.split(';')
        if len(parts) >= 2:
            categories.append({
                '–ù–∞–∑–≤–∞–Ω–∏–µ': parts[0].strip(),
                '–û–ø–∏—Å–∞–Ω–∏–µ': parts[1].strip()
            })
    
    return categories


