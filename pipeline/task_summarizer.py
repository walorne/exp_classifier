def summarize_single_task_with_retries(task_data, max_retries=3):
    """
    –°—É–º–º–∞—Ä–∏–∑–∏—Ä—É–µ—Ç –æ–¥–Ω—É –∑–∞–¥–∞—á—É —Å –ø–æ–≤—Ç–æ—Ä–Ω—ã–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏ –ø—Ä–∏ –æ—à–∏–±–∫–∞—Ö
    
    Args:
        task_data: tuple (index, task_row) - –∏–Ω–¥–µ–∫—Å –∏ –¥–∞–Ω–Ω—ã–µ –∑–∞–¥–∞—á–∏
        max_retries: –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫
    
    Returns:
        tuple: (index, summary, success, error_msg)
    """
    index, task_row = task_data
    task_key = task_row.get('key', f'Task_{index}')
    
    # –°–æ–∑–¥–∞–µ–º –æ—Ç–¥–µ–ª—å–Ω—ã–π –∫–ª–∏–µ–Ω—Ç –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –ø–æ—Ç–æ–∫–∞
    llm_client = create_default_client()
    
    for attempt in range(max_retries):
        try:
            summary = summarize_single_task(task_row, llm_client)
            return index, summary, True, None
            
        except Exception as e:
            error_msg = f"–ü–æ–ø—ã—Ç–∫–∞ {attempt + 1}/{max_retries}: {str(e)}"
            if attempt == max_retries - 1:  # –ü–æ—Å–ª–µ–¥–Ω—è—è –ø–æ–ø—ã—Ç–∫–∞
                return index, f"–û—à–∏–±–∫–∞ –ø–æ—Å–ª–µ {max_retries} –ø–æ–ø—ã—Ç–æ–∫: {str(e)}", False, error_msg
            
            # –ù–µ–±–æ–ª—å—à–∞—è –∑–∞–¥–µ—Ä–∂–∫–∞ –º–µ–∂–¥—É –ø–æ–ø—ã—Ç–∫–∞–º–∏
            time.sleep(0.5 * (attempt + 1))
    
    return index, "–û—à–∏–±–∫–∞ —Å—É–º–º–∞—Ä–∏–∑–∞—Ü–∏–∏", False, "–ü—Ä–µ–≤—ã—à–µ–Ω–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫"


